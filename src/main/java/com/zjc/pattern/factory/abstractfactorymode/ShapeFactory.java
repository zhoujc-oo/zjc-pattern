package com.zjc.pattern.factory.abstractfactorymode;


import com.zjc.pattern.factory.Circle;
import com.zjc.pattern.factory.Rectangle;
import com.zjc.pattern.factory.Shape;
import com.zjc.pattern.factory.Square;

public class ShapeFactory extends AbstractFactory {
    @Override
    public Color getColor(String color) {
        return null;
    }

    @Override
    public Shape getShape(String shapeType) {
        if (shapeType == null) {
            return null;
        }
        if (shapeType.equalsIgnoreCase("CIRCLE")) {
            return new Circle();
        } else if (shapeType.equalsIgnoreCase("RECTANGLE")) {
            return new Rectangle();
        } else if (shapeType.equalsIgnoreCase("SQUARE")) {
            return new Square();
        }
        return null;
    }
}
